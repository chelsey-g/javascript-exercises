// strings - concatenation example
// 1st example

let service = "credit card";
let month = "May 30th";
let displayText = "Your " + service + " bill is due on " + month + ".";

console.log(displayText);

// 2nd example

let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName;
console.log(fullName);

//In this example, we define two string variables firstName and lastName, and then concatenate them using the + operator and a space character. The result is stored in a new variable fullName, which is then printed to the console using the console.log() function. The output is the full name "John Doe", which is the result of concatenating the first and last names.
